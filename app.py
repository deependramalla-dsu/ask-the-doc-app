import streamlit as st
from langchain.llms import OpenAI
from langchain.text_splitter import CharacterTextSplitter
from langchain.embeddings import OpenAIEmbeddings
from langchain.vectorstores import Chroma
from langchain.chains import RetrievalQA


def generate_response(uploaded_file, openai_api_key, query_text):
    """Create a QA answer from an uploaded text file and a user query."""
    # Load document if file is uploaded
    if uploaded_file is not None:
        # Read file as text (assuming UTF-8)
        document_text = uploaded_file.read().decode("utf-8", errors="ignore")
        documents = [document_text]

        # Split documents into chunks
        text_splitter = CharacterTextSplitter(
            chunk_size=1000,
            chunk_overlap=0
        )
        texts = text_splitter.create_documents(documents)

        # Select embeddings
        embeddings = OpenAIEmbeddings(openai_api_key=openai_api_key)

        # Create a vectorstore from documents (Chroma in-memory)
        db = Chroma.from_documents(texts, embeddings)

        # Create retriever interface
        retriever = db.as_retriever()

        # Create QA chain (stuff = simplest chain type)
        llm = OpenAI(openai_api_key=openai_api_key, temperature=0)
        qa = RetrievalQA.from_chain_type(
            llm=llm,
            chain_type="stuff",
            retriever=retriever,
        )

        # Run the chain on the user query
        return qa.run(query_text)


# ----------------- Streamlit UI -----------------

# Page title
st.set_page_config(page_title="ðŸ¦œðŸ”— Ask the Doc App")
st.title("ðŸ¦œðŸ”— Ask the Doc App")

st.write(
    "Upload a **.txt** document and ask questions about it. "
    "The app will read the document and answer using OpenAI."
)

# File upload
uploaded_file = st.file_uploader("Upload an article", type="txt")

# Query text
query_text = st.text_input(
    "Enter your question:",
    placeholder="Please provide a short summary.",
    disabled=not uploaded_file,
)

# Form input and query
result = []

with st.form("myform", clear_on_submit=True):
    openai_api_key = st.text_input(
        "OpenAI API Key",
        type="password",
        disabled=not (uploaded_file and query_text),
    )

    submitted = st.form_submit_button(
        "Submit",
        disabled=not (uploaded_file and query_text),
    )

    if submitted:
        if not openai_api_key.startswith("sk-"):
            st.error("Please enter a valid OpenAI API key (starts with 'sk-').")
        else:
            with st.spinner("Calculating..."):
                response = generate_response(uploaded_file, openai_api_key, query_text)
                result.append(response)
            # Remove API key from memory for minimal safety
            del openai_api_key

# Display result
if len(result):
    st.info(result[-1])

